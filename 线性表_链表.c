/* 链表中更改某结点的数据域

链表中修改结点的数据域，通过遍历的方法找到该结点，然后直接更改数据域的值。

实现代码： */
/************************************************************************/

//更新函数，其中，add 表示更改结点在链表中的位置，newElem 为新的数据域的值
link *amendElem(link * p,int add,int newElem){
	link * temp=p;
	temp=temp->next;//在遍历之前，temp指向首元结点
	//遍历到被删除结点
	for (int i=1; i<add; i++) {
		temp=temp->next;
	}
	temp->elem=newElem;
	return p;
}

